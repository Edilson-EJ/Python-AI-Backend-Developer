# exemplo

saldo = 1000
saque = 200
limite = 100

print("saldo e maior que saque", saldo >= saque)

print("saque e menor que limite", saque <= limite)

# opreador E

print("usando o AND", saldo >= saque and saque <= limite)

# operador ou

print("usando o OR", saldo >= saque or saque <= limite)

# operador negação

print("1000 e maior 1500", not 1000 > 1500)